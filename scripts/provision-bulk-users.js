const fs = require('fs');
const path = require('path');
const Database = require('better-sqlite3');
const { loadConfig } = require('../src/config');

function parseArgs(argv) {
  const parsed = {};
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (!arg.startsWith('--')) {
      continue;
    }

    const key = arg.slice(2);
    const value = argv[i + 1];
    if (!value || value.startsWith('--')) {
      parsed[key] = 'true';
      continue;
    }

    parsed[key] = value;
    i += 1;
  }
  return parsed;
}

function toInt(value, fallback) {
  const parsed = Number.parseInt(value, 10);
  return Number.isFinite(parsed) ? parsed : fallback;
}

function toBool(value, fallback) {
  if (value === undefined || value === null || value === '') {
    return fallback;
  }

  const normalized = String(value).trim().toLowerCase();
  if (['1', 'true', 'yes', 'on'].includes(normalized)) {
    return true;
  }
  if (['0', 'false', 'no', 'off'].includes(normalized)) {
    return false;
  }
  return fallback;
}

function padNumber(value, width) {
  return String(value).padStart(width, '0');
}

function ensureDir(filePath) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
}

function ensureSchema(db) {
  const schemaPath = path.join(__dirname, '..', 'src', 'db', 'schema.sql');
  const schemaSql = fs.readFileSync(schemaPath, 'utf8');
  db.exec(schemaSql);
}

function createUsers({
  count,
  startIndex,
  endpointPrefix,
  endpointWidth,
  userIdPrefix,
  displayPrefix,
  realPrefix,
  realStart,
  realWidth
}) {
  const users = [];
  for (let i = 0; i < count; i += 1) {
    const userIndex = startIndex + i;
    const endpoint = `${endpointPrefix}${padNumber(userIndex, endpointWidth)}`;
    const userId = `${userIdPrefix}${endpoint}`;
    const realE164 = `${realPrefix}${padNumber(realStart + i, realWidth)}`;

    users.push({
      id: userId,
      display_name: `${displayPrefix} ${padNumber(userIndex, endpointWidth)}`,
      real_e164: realE164,
      caller_endpoint: endpoint,
      password: `${endpoint}-strong-password`
    });
  }
  return users;
}

function createVirtualNumbers({
  count,
  prefix,
  start,
  width
}) {
  const virtuals = [];
  for (let i = 0; i < count; i += 1) {
    const e164 = `${prefix}${padNumber(start + i, width)}`;
    virtuals.push(e164);
  }
  return virtuals;
}

function renderGeneratedPjsip(users, { useStaticContact, contactHost, contactPort }) {
  const lines = [];
  lines.push('; Auto-generated by scripts/provision-bulk-users.js');
  lines.push(`; Generated at ${new Date().toISOString()}`);
  if (useStaticContact) {
    lines.push(`; contact mode: static (${contactHost}:${contactPort})`);
  } else {
    lines.push('; contact mode: dynamic register');
  }
  lines.push('');

  users.forEach((user) => {
    const endpoint = user.caller_endpoint;
    lines.push(`[${endpoint}]`);
    lines.push('type=endpoint');
    lines.push('transport=transport-udp-privacy');
    lines.push('context=caller_in');
    lines.push('message_context=privacy_message_in');
    lines.push('disallow=all');
    lines.push('allow=ulaw');
    lines.push(`auth=${endpoint}`);
    lines.push(`aors=${endpoint}`);
    lines.push('direct_media=no');
    lines.push('disable_direct_media_on_nat=yes');
    lines.push('rtp_keepalive=20');
    lines.push('rtp_symmetric=yes');
    lines.push('force_rport=yes');
    lines.push('rewrite_contact=yes');
    lines.push('media_use_received_transport=yes');
    lines.push('');

    lines.push(`[${endpoint}]`);
    lines.push('type=auth');
    lines.push('auth_type=userpass');
    lines.push(`username=${endpoint}`);
    lines.push(`password=${user.password}`);
    lines.push('');

    lines.push(`[${endpoint}]`);
    lines.push('type=aor');
    lines.push('max_contacts=1');
    lines.push('remove_existing=yes');
    lines.push('remove_unavailable=yes');
    lines.push('qualify_frequency=30');
    if (useStaticContact) {
      lines.push(`contact=sip:${endpoint}@${contactHost}:${contactPort}`);
    }
    lines.push('');
  });

  return `${lines.join('\n').trim()}\n`;
}

function writeCredentialsCsv(filePath, users) {
  const rows = [
    'user_id,display_name,endpoint,password,real_e164'
  ];

  users.forEach((user) => {
    rows.push([
      user.id,
      user.display_name,
      user.caller_endpoint,
      user.password,
      user.real_e164
    ].join(','));
  });

  ensureDir(filePath);
  fs.writeFileSync(filePath, `${rows.join('\n')}\n`, 'utf8');
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const config = loadConfig();

  const count = Math.max(1, toInt(args.count, 100));
  const startIndex = Math.max(1, toInt(args['start-index'], 1));
  const endpointPrefix = String(args['endpoint-prefix'] || 'u').trim();
  const endpointWidth = Math.max(2, toInt(args['endpoint-width'], 4));
  const userIdPrefix = String(args['user-id-prefix'] || 'user-').trim();
  const displayPrefix = String(args['display-prefix'] || 'Load User').trim();

  const realPrefix = String(args['real-prefix'] || '+8613900').trim();
  const realStart = Math.max(0, toInt(args['real-start'], 100000));
  const realWidth = Math.max(4, toInt(args['real-width'], 6));

  const virtualCount = Math.max(1, toInt(args['virtual-count'], 30));
  const virtualPrefix = String(args['virtual-prefix'] || '+8613800').trim();
  const virtualStart = Math.max(0, toInt(args['virtual-start'], 200000));
  const virtualWidth = Math.max(4, toInt(args['virtual-width'], 6));
  const useStaticContact = toBool(args['use-static-contact'], true);
  const contactHost = String(args['contact-host'] || '127.0.0.1').trim();
  const contactPort = Math.max(1, toInt(args['contact-port'], 17060));

  const dbPath = String(args['db-path'] || config.db.path);
  const pjsipOutputPath = path.resolve(
    String(args['pjsip-output'] || path.join(__dirname, '..', 'deploy', 'asterisk', 'pjsip.generated.conf'))
  );
  const credentialsOutputPath = path.resolve(
    String(args['credentials-output'] || path.join(__dirname, '..', 'data', 'generated-users.csv'))
  );

  const users = createUsers({
    count,
    startIndex,
    endpointPrefix,
    endpointWidth,
    userIdPrefix,
    displayPrefix,
    realPrefix,
    realStart,
    realWidth
  });

  const virtualNumbers = createVirtualNumbers({
    count: virtualCount,
    prefix: virtualPrefix,
    start: virtualStart,
    width: virtualWidth
  });

  ensureDir(dbPath);
  const db = new Database(dbPath);
  db.pragma('foreign_keys = ON');
  ensureSchema(db);

  const upsertUser = db.prepare(`
    INSERT INTO users (id, display_name, real_e164, caller_endpoint, enabled, created_at, updated_at)
    VALUES (@id, @display_name, @real_e164, @caller_endpoint, 1, datetime('now'), datetime('now'))
    ON CONFLICT(id) DO UPDATE SET
      display_name = excluded.display_name,
      real_e164 = excluded.real_e164,
      caller_endpoint = excluded.caller_endpoint,
      enabled = 1,
      updated_at = datetime('now')
  `);

  const upsertVirtual = db.prepare(`
    INSERT INTO virtual_numbers (e164, enabled, created_at, updated_at)
    VALUES (?, 1, datetime('now'), datetime('now'))
    ON CONFLICT(e164) DO UPDATE SET
      enabled = 1,
      updated_at = datetime('now')
  `);

  const getVirtualByE164 = db.prepare(`
    SELECT id, e164
    FROM virtual_numbers
    WHERE e164 = ? AND enabled = 1
    LIMIT 1
  `);

  const upsertMapping = db.prepare(`
    INSERT INTO id_mappings (callee_e164, virtual_number_id, updated_at)
    VALUES (?, ?, datetime('now'))
    ON CONFLICT(callee_e164) DO UPDATE SET
      virtual_number_id = excluded.virtual_number_id,
      updated_at = datetime('now')
  `);

  const tx = db.transaction(() => {
    users.forEach((user) => upsertUser.run(user));
    virtualNumbers.forEach((e164) => upsertVirtual.run(e164));

    const virtualRows = virtualNumbers
      .map((e164) => getVirtualByE164.get(e164))
      .filter(Boolean);

    if (virtualRows.length === 0) {
      throw new Error('no enabled virtual numbers found after upsert');
    }

    users.forEach((user, idx) => {
      const chosen = virtualRows[idx % virtualRows.length];
      upsertMapping.run(user.real_e164, chosen.id);
    });
  });

  tx();
  db.close();

  const generatedPjsip = renderGeneratedPjsip(users, {
    useStaticContact,
    contactHost,
    contactPort
  });
  ensureDir(pjsipOutputPath);
  fs.writeFileSync(pjsipOutputPath, generatedPjsip, 'utf8');
  writeCredentialsCsv(credentialsOutputPath, users);

  // eslint-disable-next-line no-console
  console.log(`Provisioned users: ${users.length}`);
  // eslint-disable-next-line no-console
  console.log(`Provisioned virtual numbers: ${virtualNumbers.length}`);
  // eslint-disable-next-line no-console
  console.log(`Database: ${dbPath}`);
  // eslint-disable-next-line no-console
  console.log(`Generated PJSIP endpoints: ${pjsipOutputPath}`);
  // eslint-disable-next-line no-console
  console.log(`Static contact mode: ${useStaticContact ? `enabled (${contactHost}:${contactPort})` : 'disabled'}`);
  // eslint-disable-next-line no-console
  console.log(`Generated credentials CSV: ${credentialsOutputPath}`);
  // eslint-disable-next-line no-console
  console.log(`Example user: ${users[0].id} (${users[0].caller_endpoint} / ${users[0].real_e164})`);
  // eslint-disable-next-line no-console
  console.log('Next steps:');
  // eslint-disable-next-line no-console
  console.log('  1) sudo ./scripts/deploy-asterisk-config.sh');
  if (useStaticContact) {
    // eslint-disable-next-line no-console
    console.log('  2) npm run sip:loadbot -- --port 17060');
    // eslint-disable-next-line no-console
    console.log('  3) sudo asterisk -rx \'pjsip show contacts\'');
  }
  // eslint-disable-next-line no-console
  console.log(`  ${useStaticContact ? 4 : 2}) sudo ./scripts/sync-asterisk-astdb.sh`);
}

main();
